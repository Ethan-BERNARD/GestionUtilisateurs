    /**
    * Initialise le bouton "Ajouter" pour créer un nouvel utilisateur.
    * Vérifie les données, génère login et mot de passe, puis insère en base.
    */
    private void initialiserBtnAjouter() {
        btnAjouter.addActionListener((var e) -> {
            String id = FieldId.getText().trim();
            String nom = FieldNom.getText().trim();
            String prenom = FieldPrenom.getText().trim();
            String adresse = FieldAddr.getText().trim();
            String cp = FieldCp.getText().trim();
            String ville = FieldVille.getText().trim();
            String dateStr = FieldDate.getText().trim();

            if (id.isEmpty() || nom.isEmpty() || prenom.isEmpty() || adresse.isEmpty() ||
                cp.isEmpty() || ville.isEmpty() || dateStr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tous les champs doivent être remplis !");
                return;
            }

            if (!estDateValide(dateStr)) {
                JOptionPane.showMessageDialog(null, "Format de date invalide. Utilise yyyy-MM-dd.");
                return;
            }

            Date dateEmbauche = Date.valueOf(dateStr);
            
            if (!cp.matches("\\d{5}")) {
                JOptionPane.showMessageDialog(null, "Le code postal doit contenir exactement 5 chiffres.");
                return;
            }

            if (nom.length() < 2 || prenom.length() < 2) {
                JOptionPane.showMessageDialog(null, "Nom et prénom doivent contenir au moins 2 caractères.");
                return;
            }

            if (dateEmbauche.after(Date.valueOf(LocalDate.now()))) {
                JOptionPane.showMessageDialog(null, "La date d'embauche ne peut pas être dans le futur.");
                return;
            }
            
            if (!nom.matches("[a-zA-ZÀ-ÿ\\- ]+")) {
                JOptionPane.showMessageDialog(null, "Le nom ne doit contenir que des lettres, des tirets ou des espaces.");
                return;
            }

            String login;
            try {
                login = bd.genererLoginUnique(nom, prenom);
            } catch (IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String mdp = bd.genererMotDePasse();

            if (bd.utilisateurExiste(nom, prenom, adresse, cp, ville, dateEmbauche)) {
                JOptionPane.showMessageDialog(null, "Cet utilisateur existe déjà !");
                return;
            }

            if (bd.loginExiste(login)) {
                JOptionPane.showMessageDialog(null, "Ce login existe déjà !");
                return;
            }

            int confirmation = JOptionPane.showConfirmDialog(
                null,
                "Confirmer l'ajout de l'utilisateur :\nNom : " + nom + "\nPrénom : " + prenom +
                "\nLogin : " + login + "\nMot de passe : " + mdp,
                "Confirmation",
                JOptionPane.YES_NO_OPTION
            );

            if (confirmation == JOptionPane.YES_OPTION) {
                try {
                    bd.ajoutUtilisateurs(id, nom, prenom, login, mdp, adresse, cp, ville, dateEmbauche);
                    chargerUtilisateurs();
                    JOptionPane.showMessageDialog(null, "Utilisateur ajouté !");
                    viderChamps();
                } catch (HeadlessException ex) {
                    Logger.getLogger(getClass().getName()).log(Level.SEVERE, "Erreur lors de l'ajout de l'utilisateur", ex);
                    JOptionPane.showMessageDialog(null, "Une erreur est survenue lors de l'ajout.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ajout annulé.");
            }
        });
    }


