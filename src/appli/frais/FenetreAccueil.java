    /**
    * Initialise le comportement de la barre de recherche.
    * Gère le focus pour afficher ou masquer le texte par défaut.
    */
    private void initialiserSearchBar() {
        searchBar.setText("Recherche...");
        searchBar.setForeground(java.awt.Color.GRAY);

        searchBar.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent e) {
                if (searchBar.getText().equals("Recherche...")) {
                    searchBar.setText("");
                    searchBar.setForeground(java.awt.Color.BLACK);
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent e) {
                if (searchBar.getText().isEmpty()) {
                    searchBar.setForeground(java.awt.Color.GRAY);
                    searchBar.setText("Recherche...");
                }
            }
        });
    }

