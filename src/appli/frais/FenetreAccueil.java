    /**
    * Charge tous les utilisateurs depuis la base de données et les affiche dans le tableau.
    * <p>
    * Utilise la méthode {@link AccesBdD#getLesUtilisateurs()}.
    * En cas d'erreur SQL, affiche un message d'erreur.
    * </p>
    */
    private void chargerUtilisateurs() {
        DefaultTableModel model = (DefaultTableModel) tableUtilisateurs.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = bd.getLesUtilisateurs();
            while (rs.next()) {
                Object[] ligne = new Object[] {
                    rs.getString("id"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("login"),
                    rs.getString("mdp"),
                    rs.getString("adresse"),
                    rs.getString("cp"),
                    rs.getString("ville"),
                    rs.getString("dateEmbauche")
                };
                model.addRow(ligne);
            }
        } catch (SQLException e) {
            Logger.getLogger(getClass().getName()).log(Level.SEVERE, "Erreur lors du chargement des utilisateurs", e);
            JOptionPane.showMessageDialog(null, "Erreur lors du chargement des utilisateurs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Gère l'action lors de la saisie dans la barre de recherche.
     * <p>
     * Recherche les utilisateurs selon le critère sélectionné (ID ou Nom)
     * et met à jour le tableau en conséquence.
     * </p>
     *
     * @param evt l'événement déclenché par la barre de recherche
     */
    private void searchBarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBarActionPerformed
        String recherche = searchBar.getText().trim().toLowerCase();

        if (recherche.equals("recherche...")) {
            recherche = "";
        }

        DefaultTableModel tableModel = (DefaultTableModel) tableUtilisateurs.getModel();
        tableModel.setRowCount(0);

        if (recherche.isEmpty()) {
            listerLesUtilisateurs();
            return;
        }

        try (ResultSet rs = radioNom.isSelected() 
                            ? bd.getLesUtilisateursUnNom(recherche)
                            : radioId.isSelected() 
                                ? bd.getLesUtilisateursUnId(recherche)
                                : null) {

            if (rs == null) {
                JOptionPane.showMessageDialog(this, "Veuillez sélectionner un critère de recherche.");
                return;
            }

            while (rs.next()) {
                Object[] utilisateur = {
                    rs.getString("id"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("login"),
                    rs.getString("mdp"),
                    rs.getString("adresse"),
                    rs.getString("cp"),
                    rs.getString("ville"),
                    rs.getDate("dateEmbauche")
                };
                tableModel.addRow(utilisateur);
            }

            if (tableModel.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Aucun utilisateur trouvé pour \"" + recherche + "\".");
            }

        } catch (SQLException ex) {
            Logger.getLogger(FenetreAccueil.class.getName()).log(Level.SEVERE, "Erreur lors de la recherche d'utilisateur", ex);
            JOptionPane.showMessageDialog(this, "Erreur lors de la recherche en base de données.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }

        tableUtilisateurs.setModel(tableModel);
    }//GEN-LAST:event_searchBarActionPerformed
    
